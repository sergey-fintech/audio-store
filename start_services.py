        auth_dir = self.base_dir / "services" / "auth"
        
        if not auth_dir.exists():
            print(f"‚ùå –ü–∞–ø–∫–∞ {auth_dir} –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!")
