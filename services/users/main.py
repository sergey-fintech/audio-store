# Этот сервис отвечает за регистрацию, авторизацию и управление профилями пользователей, включая:
# - Регистрацию новых пользователей
# - Авторизацию и аутентификацию (JWT токены)
# - Управление профилями пользователей
# - Восстановление паролей
# - Личный кабинет с историей покупок
# - Управление библиотекой приобретенных книг
# - Настройки пользователя и уведомления

from fastapi import FastAPI

app = FastAPI(title="Users Service", version="1.0.0")

@app.get("/")
async def root():
    return {"message": "Users Service is running"}

if __name__ == "__main__":
    import uvicorn
    uvicorn.run(app, host="0.0.0.0", port=8002) 